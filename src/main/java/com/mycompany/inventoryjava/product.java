    private void UpdateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateBtnMouseClicked
        // TODO add your handling code here:
        if(ProductID.getText().isEmpty()||ProductName.getText().isEmpty()||ProductQty.getText().isEmpty()||ProductDesc.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Missing Information");
        }
        else{
        try{
            Con=DriverManager.getConnection("jdbc:mysql://localhost:3306/inventorydb","jay","0000");
            String UpdateQuery="update inventorydb.producttbl set ProductName='"+ProductName.getText()+"'"+",ProductQty="+ProductQty.getText()+",ProductDesc='"+ProductDesc.getText()+"',ProductCategory='"+ProductCategory.getSelectedItem().toString()+"'where ProductID="+ProductID.getText();
            Statement Add=Con.createStatement();
            Add.executeUpdate(UpdateQuery);
            JOptionPane.showMessageDialog(this,"Product added Successfully");
            SelectProduct();
        }catch(Exception e){
                e.printStackTrace();
                }
        }
    }//GEN-LAST:event_UpdateBtnMouseClicked

