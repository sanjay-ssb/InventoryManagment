    public void SelectProduct(){
         
        try{
            Con=DriverManager.getConnection("jdbc:mysql://localhost:3306/inventorydb","jay","0000");
            St=Con.createStatement();
            Rs=St.executeQuery("select * from producttbl");
            //producttbl.setModel(DbUtils);
            producttbl.setModel(net.proteanit.sql.DbUtils.resultSetToTableModel(Rs));  
        }catch(SQLException e){
            e.printStackTrace();
        }
    }
    private void getCat(){
        
        try{
            Con=DriverManager.getConnection("jdbc:mysql://localhost:3306/inventorydb","jay","0000");
        St=Con.createStatement();
        String Query="Select * from inventorydb.categorytbl";
        Rs=St.executeQuery(Query);
        while(Rs.next())
        {
            String MyCat=Rs.getString("CategoryName");
            ProductCategory.addItem(MyCat);
        }
        }catch(SQLException e){e.printStackTrace();}
        
        
        
    }
    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked
        // TODO add your handling code here:
        try{
            Con=DriverManager.getConnection("jdbc:mysql://localhost:3306/inventorydb","jay","0000");
            PreparedStatement add=Con.prepareStatement("insert into producttbl values(?,?,?,?,?)");
            add.setInt(1,Integer.valueOf(ProductID.getText()));
            add.setString(2,ProductName.getText());
            add.setInt(3,Integer.valueOf(ProductQty.getText()));
            add.setString(4,ProductDesc.getText());
            add.setString(5,ProductCategory.getSelectedItem().toString());
            int row=add.executeUpdate();
            JOptionPane.showMessageDialog(this,"Product added Successfully");
            Con.close();
            SelectProduct();
        }catch(SQLException e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_AddBtnMouseClicked

    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        // TODO add your handling code here:
        if(ProductID.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Enter the product to be deleted");
        }
        else{
            try{
                Con=DriverManager.getConnection("jdbc:mysql://localhost:3306/inventorydb","jay","0000");
                String id=ProductID.getText();
                String Query="Delete From inventorydb.producttbl where ProductID="+id;
                Statement Add=Con.createStatement();
                Add.executeUpdate(Query);
                SelectProduct();
                JOptionPane.showMessageDialog(this,"Product Deleted Successfully");
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_DeleteBtnMouseClicked

